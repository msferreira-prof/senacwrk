    private void jButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarActionPerformed
        ApoliceService as = new ApoliceService();
        Apolice apolice = as.consultarPorNumero(Long.parseLong(jTextFieldNumero.getText()));
        if (apolice != null) {
            jTextFieldApoliceSeguradoNome.setText(apolice.getSegurado().getNome());
            jTextFieldApoliceUf.setText(apolice.getUf());
            jTextFieldValorSegurado.setText(Double.toString(apolice.getValorSegurado()));
            jTextFieldValorPremio.setText(Double.toString(apolice.getValorPremio()));
            jTextFieldValorDesconto.setText(Double.toString(apolice.getValorDesconto()));
            jButtonMostrarSegurado.setEnabled(true);
        } else {
            String mensagem = String.format("Apolice %s nao encontrada", jTextFieldNumero.getText());
            JOptionPane.showMessageDialog(this, mensagem);
        }
    }//GEN-LAST:event_jButtonConsultarActionPerformed

