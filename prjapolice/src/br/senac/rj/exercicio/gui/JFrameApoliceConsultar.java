    private void jButtonMostrarSeguradoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMostrarSeguradoActionPerformed
        if (jFrameSeguradoApresentar == null) {
            jFrameSeguradoApresentar = new JFrameSeguradoApresentar();
        }
        
        Segurado novoSegurado = new Segurado();
        SeguradoService ss = new SeguradoService();
        Segurado seguradoApolice = ss.consultarPorNome(jTextFieldApoliceSeguradoNome.getText());
        if (seguradoApolice != null) {
            jFrameSeguradoApresentar.setSeguradoExibicao(seguradoApolice);
            jFrameSeguradoApresentar.setVisible(true);
        } else {          
            JOptionPane.showMessageDialog(this, "Segurado nao encontrado!");
        }
    }//GEN-LAST:event_jButtonMostrarSeguradoActionPerformed

